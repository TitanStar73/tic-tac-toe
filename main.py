class Board:
    def __init__(self,X,O,B) -> None:
        self.X = X
        self.O = O
        self.B = B
        # 0 = blank, 1 = X, 2 = 0
        self.board = [[0,0,0],[0,0,0],[0,0,0]]
    
    #places piece on the board
    def place_piece(self,loc, X):
        if loc == 1:
            self.board[0][0] = X
        elif loc == 2:
            self.board[0][1] = X
        elif loc == 3:
            self.board[0][2] = X
        elif loc == 4:
            self.board[1][0] = X
        elif loc == 5:
            self.board[1][1] = X
        elif loc == 6:
            self.board[1][2] = X
        elif loc == 7:
            self.board[2][0] = X
        elif loc == 8:
            self.board[2][1] = X
        elif loc == 9:
            self.board[2][2] = X

    def print_board_raw(self):
        for row in self.board:
            print(row)

